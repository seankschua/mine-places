
public class Entity {
	
	public String name;
	public int count;
	public int score;
	
	public Entity(String name, int count, int score) {
		this.name = name;
		this.count = count;
		this.score = score;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}
	
	
	
}
